// ==================================================
// ARITHMETIC OPERATORS IN JAVASCRIPT
// ==================================================
// Arithmetic operators are used to perform
// mathematical calculations on numbers.

// Addition (+)
// Adds two values
let sum = 5 + 3;
console.log("sum :", sum); // OP: 8

// Subtraction (-)
// Subtracts the right value from the left
let diff = 5 - 3;
console.log("diff :", diff); // OP: 2

// Multiplication (*)
// Multiplies two values
let product = 5 * 3;
console.log("product :", product); // OP: 15

// Division (/)
// Divides left value by right value
let quotient = 10 / 2;
console.log("quotient :", quotient); // OP: 5

// Division can return decimal values
let decimalQuotient = 10 / 3;
console.log("decimalQuotient :", decimalQuotient); // OP: 3.333...

// Math.floor()
// Returns only the integer part of a number
let floorQuotient = Math.floor(10 / 3);
console.log("floorQuotient :", floorQuotient); // OP: 3

// Modulus (%)
// Returns the remainder after division
let remainder = 10 % 3;
console.log("remainder :", remainder); // OP: 1

// Modulus is commonly used to check even/odd numbers
let evenCheck = 10 % 2; // 0 means even
let oddCheck = 7 % 2;   // 1 means odd

console.log("evenCheck :", evenCheck); // OP: 0
console.log("oddCheck :", oddCheck);   // OP: 1
