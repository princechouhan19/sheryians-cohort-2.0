let str = "Prince";
console.log(str.substr(5,2)); // e (starts at index 5 and takes 2 characters)
console.log(str.substr(2)); // ince (starts at index 2, takes till end)
console.log(str.substr(-4,2)); // in (starts 4th from end, takes 2 characters)
console.log(str.substr(-5));   // rince (starts 5th from end, takes till end)   